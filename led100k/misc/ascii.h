#ifndef ASCII_H
#define ASCII_H

#define ASCII_NUL	0 		// Strg-@ 		Null prompt
#define ASCII_SOH	1 		// Strg-A 		Start of heading
#define ASCII_STX	2 		// Strg-B 		Start of text
#define ASCII_ETX	3 		// Strg-C 		End of Text
#define ASCII_EOT	4 		// Strg-D 		End of transmission
#define ASCII_ENQ	5 		// Strg-E 		Enquiry
#define ASCII_ACK	6 		// Strg-F 		Acknowledge
#define ASCII_BEL	7 		// Strg-G 		Bell
#define ASCII_BS 	8 		// Strg-H 		Backspace
#define ASCII_HT 	9 		// Strg-I 		Horizontal tab
#define ASCII_LF 	10 		// Strg-J 		Line feed
#define ASCII_VT 	11 		// Strg-K 		Vertical tab
#define ASCII_FF 	12 		// Strg-L 		Form feed / NP New page
#define ASCII_CR 	13 		// Strg-M 		Carriage return
#define ASCII_SO 	14 		// Strg-N 		Shift out
#define ASCII_SI 	15 		// Strg-O 		Shift in
#define ASCII_DLE	16 		// Strg-P 		Data link escape
#define ASCII_DC1	17 		// Strg-Q 		X-ON
#define ASCII_DC2	18 		// Strg-R 		 
#define ASCII_DC3	19 		// Strg-S 		X-Off
#define ASCII_DC4	20 		// Strg-T 		 
#define ASCII_NAK	21 		// Strg-U 		No achnowledge
#define ASCII_SYN	22 		// Strg-V 		Synchronous idle
#define ASCII_ETB	23 		// Strg-W 		End transmission blocks
#define ASCII_CAN	24 		// Strg-X 		Cancel
#define ASCII_EM 	25 		// Strg-Y 		End of medium
#define ASCII_SUB	26 		// Strg-Z 		Substitute
#define ASCII_ESC	27 		// Strg-[ 		Escape
#define ASCII_FS 	28 		// Strg-\ 		File separator
#define ASCII_GS 	29 		// Strg-] 		Group separator
#define ASCII_RS 	30 		// Strg-^ 		Record separator
#define ASCII_US 	31 		// Strg-_ 		Unit separator
#define ASCII_DEL	127 	//				Delete or rubout 

#endif
